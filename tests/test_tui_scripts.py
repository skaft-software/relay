def test_script_with_cpu_moe(scripts_tmp, monkeypatch, tui):
    """When launch_flags include --cpu-moe, it appears in the script."""
    monkeypatch.setattr(tui, "companion_for", _no_companion)
    monkeypatch.setattr(tui, "IS_MAC", True)

    tui.write_start_script(
        "big-model", Path("/m/models/model.gguf"),
        Path("/m/models"), Path("/opt/llama.cpp/llama-server"),
        Path("/opt/llama.cpp"), 8084, LAUNCH_FLAGS_CPU_MOE)

    content = (scripts_tmp / "start-big-model.sh").read_text()
    assert "--cpu-moe" in content


def test_linux_returns_container_path(scripts_tmp, monkeypatch, tui):
    """On Linux (IS_MAC=False), the path is inside the container."""
    monkeypatch.setattr(tui, "companion_for", _no_companion)
    monkeypatch.setattr(tui, "IS_MAC", False)

    out = tui.write_start_script(
        "container-model", Path("/m/models/model.gguf"),
        Path("/m/models"), Path("/opt/llama.cpp/llama-server"),
        Path("/opt/llama.cpp"), 8085, LAUNCH_FLAGS)

    assert out == "/relay/start-scripts/start-container-model.sh"
    local_path = scripts_tmp / "start-container-model.sh"
    assert local_path.exists()


def test_render_compose_structure(monkeypatch, tui):
    """render_compose produces valid docker-compose with expected structure."""
    models_dir = Path("/m/models")
    llama_root = Path("/opt/llama.cpp")
    compose = tui.render_compose(models_dir, llama_root)

    assert "services:" in compose
    assert "relay:" in compose
    assert "image: relay-gateway:local" in compose
    assert "container_name: relay" in compose
    assert "restart: unless-stopped" in compose
    assert "network_mode: host" in compose
    assert "pid: host" in compose
    assert "env_file:" in compose
    assert "- .env" in compose
    assert "volumes:" in compose
    assert f"{models_dir}:{models_dir}:ro" in compose
    assert f"{llama_root}:{llama_root}:ro" in compose
    assert "/relay/start-scripts:ro" in compose
