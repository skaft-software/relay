"""Golden-file tests for write_start_script and render_compose.

These verify that the per-model launch scripts and docker-compose output
the TUI generates contain the expected structure and correctness-critical
flags (--jinja, kv cache type, flash-attn, etc.).
"""
import os
import stat
from pathlib import Path

import pytest


LAUNCH_FLAGS = [
    "--jinja",
    "--ctx-size", "98304",
    "-ngl", "999",
    "--parallel", "1",
    "--flash-attn", "on",
    "--cache-type-k", "q4_0",
    "--cache-type-v", "q4_0",
]

LAUNCH_FLAGS_CPU_MOE = LAUNCH_FLAGS + ["--cpu-moe"]


@pytest.fixture
def scripts_tmp(tmp_path, monkeypatch, tui):
    """Scratch SCRIPTS_DIR so tests don't write into ~/.relay."""
    monkeypatch.setattr(tui, "SCRIPTS_DIR", tmp_path)
    return tmp_path


def _no_companion(*args):
    return None


def test_basic_script_structure(scripts_tmp, monkeypatch, tui):
    """Generated script has shebang, cd, exec, model, host/port, flags, '$@'."""
    monkeypatch.setattr(tui, "companion_for", _no_companion)
    monkeypatch.setattr(tui, "IS_MAC", True)  # return host path, not container path

    out = tui.write_start_script(
        "my-model", Path("/m/models/model.gguf"),
        Path("/m/models"), Path("/opt/llama.cpp/llama-server"),
        Path("/opt/llama.cpp"), 8081, LAUNCH_FLAGS)

    assert out.endswith("start-my-model.sh")
    script_path = scripts_tmp / "start-my-model.sh"
    assert script_path.exists()
    content = script_path.read_text()

    assert "#!/usr/bin/env bash" in content
    assert "# relay model: my-model  (generated by relay setup)" in content
    assert 'cd "/opt/llama.cpp"' in content
    assert 'exec "/opt/llama.cpp/llama-server"' in content
    assert '--model "/m/models/model.gguf"' in content
    assert '--host 127.0.0.1 --port "${LLAMA_PORT:-8081}"' in content
    assert "--jinja" in content
    assert "--ctx-size 98304" in content
    assert "-ngl 999" in content
    assert "--cache-type-k q4_0" in content
    assert "--cache-type-v q4_0" in content
    assert '"$@"' in content
    # executable
    assert script_path.stat().st_mode & stat.S_IXUSR


def test_script_with_mmproj(scripts_tmp, monkeypatch, tui):
    """When a companion mmproj is found, --mmproj is added."""
    mmproj = Path("/m/models/mmproj-model.gguf")
    monkeypatch.setattr(tui, "companion_for", lambda gguf, kind: mmproj if kind == "mmproj" else None)
    monkeypatch.setattr(tui, "IS_MAC", True)

